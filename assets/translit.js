function translit() {
	if(source.val().match(/[А-я]/)) {
		res.val(
			source.val()
				.toLowerCase()
				.replace(/а/g, 'a')
				.replace(/б/g, 'b')
				.replace(/в/g, 'v')
				.replace(/г/g, 'g')
				.replace(/д/g, 'd')
				.replace(/е/g, 'e')
				.replace(/ё/g, 'yo')
				.replace(/ж/g, 'j')
				.replace(/з/g, 'z')
				.replace(/и/g, 'i')
				.replace(/й/g, 'j')
				.replace(/к/g, 'k')
				.replace(/л/g, 'l')
				.replace(/м/g, 'm')
				.replace(/н/g, 'n')
				.replace(/о/g, 'o')
				.replace(/п/g, 'p')
				.replace(/р/g, 'r')
				.replace(/с/g, 's')
				.replace(/т/g, 't')
				.replace(/у/g, 'u')
				.replace(/ф/g, 'f')
				.replace(/х/g, 'h')
				.replace(/ц/g, 'c')
				.replace(/ч/g, 'ch')
				.replace(/ш/g, 'sh')
				.replace(/щ/g, 'sch')
				.replace(/ъ/g, '\'\'')
				.replace(/ы/g, 'y')
				.replace(/ь/g, '\'')
				.replace(/э/g, 'e')
				.replace(/ю/g, 'ju')
				.replace(/я/g, 'ja')
		);
	}
	else {
		res.val(
			source.val()
				.toLowerCase()
				.replace(/a/g, 'а')
				.replace(/b/g, 'б')
				.replace(/c/g, 'ц')
				.replace(/d/g, 'д')
				.replace(/e/g, 'е')
				.replace(/f/g, 'ф')
				.replace(/g/g, 'г')
				.replace(/sh/g, 'ш')
				.replace(/sch/g, 'щ')
				.replace(/h/g, 'х')
				.replace(/i/g, 'и')
				.replace(/j/g, 'ж')
				.replace(/k/g, 'к')
				.replace(/l/g, 'л')
				.replace(/m/g, 'м')
				.replace(/n/g, 'н')
				.replace(/yo/g, 'ё')
				.replace(/o/g, 'о')
				.replace(/p/g, 'п')
				.replace(/q/g, 'к')
				.replace(/r/g, 'р')
				.replace(/s/g, 'с')
				.replace(/t/g, 'т')
				.replace(/u/g, 'у')
				.replace(/v/g, 'в')
				.replace(/w/g, 'в')
				.replace(/x/g, 'кс')
				.replace(/y/g, 'ы')
				.replace(/z/g, 'з')
				.replace(/\'\'/g, 'ъ')
				.replace(/\'\'/g, 'ь')
		);
	}
}

