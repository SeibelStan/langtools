function morzeRus() {
	if(source.val().match(/[А-я0-9]/)) {
		res.val(
			source.val()
				.toLowerCase()

				.replace(/\s/g, '  ')

				.replace(/,/g, '.-.-.- ')
				.replace(/:/g, '---... ')
				.replace(/;/g, '-.-.-. ')
				.replace(/\(/g, '-.--.- ')
				.replace(/\)/g, '-.--.- ')
				.replace(/\'/g, '.----. ')
				.replace(/\"/g, '.-..- ')
				.replace(/\s-\s/g, ' — ')
				.replace(/—/g, '-....- ')
				.replace(/\//g, '-..-. ')
				.replace(/\?/g, '..--.. ')
				.replace(/!/g, '--..-- ')
				.replace(/@/g, '.--.-. ')

				.replace(/1/g, '.---- ')
				.replace(/2/g, '..--- ')
				.replace(/3/g, '...-- ')
				.replace(/4/g, '....- ')
				.replace(/5/g, '..... ')
				.replace(/6/g, '-.... ')
				.replace(/7/g, '--... ')
				.replace(/8/g, '---.. ')
				.replace(/9/g, '----. ')
				.replace(/0/g, '----- ')

				.replace(/а/g, '.- ')
				.replace(/б/g, '-... ')
				.replace(/в/g, '.-- ')
				.replace(/г/g, '--. ')
				.replace(/д/g, '-.. ')
				.replace(/е/g, '. ')
				.replace(/ё/g, '. ')
				.replace(/ж/g, '...- ')
				.replace(/з/g, '--.. ')
				.replace(/и/g, '.. ')
				.replace(/й/g, '.--- ')
				.replace(/к/g, '-.- ')
				.replace(/л/g, '.-.. ')
				.replace(/м/g, '-- ')
				.replace(/н/g, '-. ')
				.replace(/о/g, '--- ')
				.replace(/п/g, '.--. ')
				.replace(/р/g, '.-. ')
				.replace(/с/g, '... ')
				.replace(/т/g, '- ')
				.replace(/у/g, '..- ')
				.replace(/ф/g, '..-. ')
				.replace(/х/g, '.... ')
				.replace(/ц/g, '-.-. ')
				.replace(/ч/g, '---. ')
				.replace(/ш/g, '---- ')
				.replace(/щ/g, '--.- ')
				.replace(/ъ/g, '--.-- ')
				.replace(/ы/g, '-.-- ')
				.replace(/ь/g, '-..- ')
				.replace(/э/g, '..-.. ')
				.replace(/ю/g, '..-- ')
				.replace(/я/g, '.-.- ')
		);
	}
	else {
		res.val(
			source.val()
				.replace(/$/g, ' ')

				.replace(/\.-\.-\.-\s/g, ',')
				.replace(/---\.\.\.\s/g, ':')
				.replace(/-\.-\.-\.\s/g, ';')
				.replace(/-\.--\.-\s\s/g, ') ')
				.replace(/\s-\.--\.-\s/g, ' (')
				.replace(/\.----\.\s/g, '\'')
				.replace(/\.-\.\.-\s/g, '\"')
				.replace(/-\.\.\.\.-\s/g, ' — ')
				.replace(/-\.\.-\.\s/g, '/')
				.replace(/\.\.--\.\.\s/g, '?')
				.replace(/--\.\.--\s/g, '!')
				.replace(/\.--\.-\.\s/g, '@')

				.replace(/\.----\s/g, '1')
				.replace(/\.\.---\s/g, '2')
				.replace(/\.\.\.--\s/g, '3')
				.replace(/\.\.\.\.-\s/g, '4')
				.replace(/\.\.\.\.\.\s/g, '5')
				.replace(/-\.\.\.\.\s/g, '6')
				.replace(/--\.\.\.\s/g, '7')
				.replace(/---\.\.\s/g, '8')
				.replace(/----\.\s/g, '9')
				.replace(/-----\s/g, '0')

				.replace(/--\.--\s/g, 'ъ')
				.replace(/\.\.-\.\.\s/g, 'э')

				.replace(/\.-\.\.\s/g, 'л')
				.replace(/\.--\.\s/g, 'п')
				.replace(/\.\.-\.\s/g, 'ф')
				.replace(/\.\.\.\.\s/g, 'х')
				.replace(/-\.-\.\s/g, 'ц')
				.replace(/---\.\s/g, 'ч')
				.replace(/----\s/g, 'ш')
				.replace(/--\.-\s/g, 'щ')
				.replace(/-\.--\s/g, 'ы')
				.replace(/-\.\.-\s/g, 'ь')
				.replace(/\.\.--\s/g, 'ю')
				.replace(/\.-\.-\s/g, 'я')
				.replace(/-\.\.\.\s/g, 'б')
				.replace(/\.\.\.-\s/g, 'ж')
				.replace(/--\.\.\s/g, 'з')
				.replace(/\.---\s/g, 'й')

				.replace(/\.\.\.\s/g, 'с')
				.replace(/---\s/g, 'о')
				.replace(/\.--\s/g, 'в')
				.replace(/-\.\.\s/g, 'д')
				.replace(/--\.\s/g, 'г')
				.replace(/\.-\.\s/g, 'р')
				.replace(/\.\.-\s/g, 'у')
				.replace(/-\.-\s/g, 'к')

				.replace(/\.\.\s/g, 'и')
				.replace(/--\s/g, 'м')
				.replace(/\.-\s/g, 'а')
				.replace(/-\.\s/g, 'н')

				.replace(/-\s/g, 'т')
				.replace(/\.\s/g, 'е')
				.replace(/\.\s/g, 'ё')

				.replace(/\s\s/g, ' ')
		);
	}
}

$(function () {
	langButtonAdd({text: 'Морзе рус.', funct: 'morzeRus()'});
});
